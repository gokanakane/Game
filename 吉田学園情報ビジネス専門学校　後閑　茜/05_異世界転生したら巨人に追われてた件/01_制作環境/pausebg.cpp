CPauseBg *CPauseBg::Create()
{
	CPauseBg *pPauseBg = NULL;	// ポインタの生成

	// シーンの生成
	if (pPauseBg == NULL)
	{
		pPauseBg = new CPauseBg;
		if (pPauseBg != NULL)
		{
			pPauseBg->Init();
		}
	}

	return pPauseBg;
}

