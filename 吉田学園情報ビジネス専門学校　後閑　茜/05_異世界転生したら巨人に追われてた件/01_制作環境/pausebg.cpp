HRESULT CPauseBg::Init(void)
{
	CScene2D::SetInitAll(D3DXVECTOR3(SCREEN_WIDTH / 2, SCREEN_HEIGHT / 2, 0.0f),
		D3DXVECTOR3(SCREEN_WIDTH / 2, SCREEN_HEIGHT / 2, 0.0f), D3DXCOLOR(0.0f, 0.0f, 0.0f, 0.5f), D3DXVECTOR2(1.0f, 1.0f));		// 初期値設定
	CScene2D::Init();

	return S_OK;
}

